def dart_map(d, indent=4):
    """Convert Python dict to Dart Map literal string"""
    prefix = ' ' * indent
    items = []
    for k, v in d.items():
        if k == 'voice_script':
            items.append(f"{prefix}'{k}': '''\n{v}\n''',")
        elif k in ('summary',):
            items.append(f"{prefix}'{k}': '''\n{v}\n''',")
        elif k == 'quiz':
            quiz_items = dart_quiz(v, indent+4)
            items.append(f"{prefix}'{k}': {quiz_items},")
        elif isinstance(v, str):
            items.append(f"{prefix}'{k}': '{q(v)[1:-1]}',")
        elif isinstance(v, int):
            items.append(f"{prefix}'{k}': {v},")
        elif isinstance(v, list):
            if not v:
                items.append(f"{prefix}'{k}': [],")
            else:
                lst_items = []
                for elem in v:
                    if isinstance(elem, dict):
                        lst_items.append(dart_map(elem, indent+6))
                    elif isinstance(elem, str):
                        lst_items.append(f"{' '*(indent+6)}'{q(elem)[1:-1]}'")
                inner = ',\n'.join(lst_items)
                if len(v) == 1:
                    items.append(f"{prefix}'{k}': [\n{inner}\n{' '*(indent+4)}],")
                else:
                    items.append(f"{prefix}'{k}': [\n{inner},\n{' '*(indent+4)}],")
    return '{\n' + ',\n'.join(items) + f'\n{" "*(indent-4)}}}'

def dart_quiz(q, indent=4):
    p = ' ' * indent
    parts = []
    for qtype, qlist in q.items():
        items = []
        for item in qlist:
            if isinstance(item, tuple):
                # MCQ tuple: (question, options list, correctIndex, explanation, difficulty)
                if qtype == 'mcq':
                    inner = f"{p*2}'question': '''{item[0]}''', 'options': {j(item[1])}, 'correctIndex': {item[2]}, 'marks': 1, 'explanation': '''{item[3]}''', 'difficulty': '{item[4]}'"
                else:
                    # Very Short tuple: (question, answer)
                    inner = f"{p*2}'question': '''{item[0]}''', 'answer': '''{item[1]}''', 'marks': 1"
                items.append(f"{' '*(indent+4)}{{\n{inner}\n{' '*(indent+4)}}}")
            elif isinstance(item, dict):
                # Short/Evaluation/Explanatory: dict with question, answer, optional key_points/marks
                inner_items = []
                for k, v in item.items():
                    if isinstance(v, str):
                        inner_items.append(f"{' '*(indent+6)}'{k}': '''{v}'''")
                    elif isinstance(v, list):
                        inner_items.append(f"{' '*(indent+6)}'{k}': {j(v)}")
                    else:
                        inner_items.append(f"{' '*(indent+6)}'{k}': {v}")
                items.append(f"{' '*(indent+4)}{{\n{', '.join(inner_items)}\n{' '*(indent+4)}}}")
        if items:
            parts.append(f"{p}'{qtype}': [\n{',\n'.join(items)},\n{p}],")
    return '{\n' + '\n'.join(parts) + f'\n{" "*(indent-4)}}}'

