def build_quiz(topic_data):
    out = ""
    out += "'quiz': {\n"
    out += "      'mcq': [\n"
    for q in topic_data.get('MCQ', []):
        out += "        {'question': " + repr(q[0]) + ", 'options': " + repr(q[1]) + ", 'correctIndex': " + str(q[2]) + ", 'marks': 1, 'explanation': " + repr(q[3]) + ", 'difficulty': '" + q[4] + "'},\n"
    out += "      ],\n"
    out += "      'very_short_1mark': [\n"
    for i, q in enumerate(topic_data.get('VS', [])):
        out += "        {'question': " + repr(q) + ", 'answer': " + repr(topic_data['VS_A'][i]) + ", 'marks': 1},\n"
    out += "      ],\n"
    out += "      'short_2mark': [\n"
    for s in topic_data.get('SHORT', []):
        out += "        {'question': " + repr(s[0]) + ", 'answer': " + repr(s[1]) + ", 'marks': 2, 'key_points': " + repr(s[2]) + "},\n"
    out += "      ],\n"
    out += "      'evaluation_4mark': [\n"
    for e in topic_data.get('EVAL', []):
        out += "        {'question': " + repr(e[0]) + ", 'answer': " + repr(e[1]) + ", 'marks': 4},\n"
    out += "      ],\n"
    out += "      'explanatory_8mark': [\n"
    for e in topic_data.get('EXPL', []):
        out += "        {'question': " + repr(e[0]) + ", 'answer': " + repr(e[1]) + ", 'marks': 8},\n"
    out += "      ],\n"
    out += "    },"
    return out

