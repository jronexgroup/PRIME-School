# Now replace each topic's quiz in the Dart file
for topic_id, topic_data in TOPICS.items():
    func_start = dart.find(f"Map<String, dynamic> {topic_id}()")
    if func_start == -1:
        print(f"ERROR: Could not find {topic_id}()")
        sys.exit(1)
    quiz_start = dart.find("'quiz': {", func_start)
    exam_tips_start = dart.find("'exam_tips':", quiz_start)
    if quiz_start == -1 or exam_tips_start == -1:
        print(f"ERROR: Could not find quiz boundaries in {topic_id}")
        sys.exit(1)

    before = dart[:quiz_start]
    after = dart[exam_tips_start:]
    dart = before + build_quiz(topic_data) + after

with open('scripts/seed_ch1_all.dart', 'w') as f:
    f.write(dart)

