Future<void> createDocument(
    String collectionPath, String documentId, Map<String, dynamic> data) async {
  final url = '$baseUrl/$collectionPath?documentId=$documentId&key=$apiKey';

  final fields = <String, dynamic>{};
  data.forEach((key, value) {
    fields[key] = _encodeValue(value);
  });

  final body = jsonEncode({'fields': fields});

  final client = HttpClient();
  try {
    final request = await client.postUrl(Uri.parse(url));
    request.headers.contentType = ContentType.json;
    request.write(body);
    final response = await request.close();
    final responseBody = await response.transform(utf8.decoder).join();

    if (response.statusCode == 200 || response.statusCode == 201) {
      print('  ✅ Created: $collectionPath/$documentId');
    } else {
      print('  ❌ Failed ($collectionPath/$documentId): ${response.statusCode} $responseBody');
    }
  } catch (e) {
    print('  ❌ Error ($collectionPath/$documentId): $e');
  } finally {
    client.close();
  }
}

Map<String, dynamic> _encodeValue(dynamic value) {
  if (value is String) {
    return {'stringValue': value};
  } else if (value is int) {
    return {'integerValue': value.toString()};
  } else if (value is double) {
    return {'doubleValue': value};
  } else if (value is bool) {
    return {'booleanValue': value};
  } else if (value is List) {
    return {
      'arrayValue': {
        'values': value.map((v) => _encodeValue(v)).toList(),
      }
    };
  } else if (value is Map) {
    final mapFields = <String, dynamic>{};
    value.forEach((k, v) {
      mapFields[k.toString()] = _encodeValue(v);
    });
    return {'mapValue': {'fields': mapFields}};
  }
  return {'nullValue': null};
}
