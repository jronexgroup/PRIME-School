def dart_str(s):
    return "'''" + s.replace("'''", "'\\''") + "'''"

def serialize(v, indent=0):
    """Serialize to valid Dart code."""
    pad = '  ' * indent
    inner = '  ' * (indent + 1)

    if isinstance(v, str):
        return dart_str(v)
    elif isinstance(v, (int, float)):
        return str(v)
    elif isinstance(v, bool):
        return 'true' if v else 'false'
    elif isinstance(v, list):
        if not v:
            return '[]'
        lines = [f'{pad}[']
        for item in v:
            lines.append(f'{inner}{serialize(item, indent+1)},')
        lines.append(f'{pad}]')
        return '\n'.join(lines)
    elif isinstance(v, dict):
        if not v:
            return '{}'
        lines = [f'{pad}{{']
        for k, val in v.items():
            lines.append(f'{inner}{dart_str(k)}: {serialize(val, indent+1)},')
        lines.append(f'{pad}}}')
        return '\n'.join(lines)
    elif isinstance(v, tuple):
        return serialize(list(v), indent)
    else:
        return dart_str(str(v))

